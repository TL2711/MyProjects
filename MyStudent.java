    public String createID() {
		char[] text = new char[5];
		for (int i = 0; i < 5; i++) {
			text[i] = digits.charAt(rand.nextInt(digits.length()));
		}
		ID = (String) this.Adyear.getSelectedItem();
		for (int i = 0; i < 5; i++) {
			ID += text[i];
		}
		return ID;
	}
    
