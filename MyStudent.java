    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        try {
            FileWriter writer = new FileWriter("student.csv");
            for(MyStudent student:studentlist) {
                writer.write(student.toString() + "\n");
            }
            System.out.println("Sucessfully Saved!");
            writer.close();
        }
        catch (IOException e) {
            System.out.println("An error occured.");
            e.printStackTrace();
        }
        try {
            File file = new File("student.csv");
            Scanner reader = new Scanner(file);
            while(reader.hasNext()){
                String line = reader.nextLine();
                String[] lineSplit = line.split(",");
                String name = lineSplit[0].trim();
                String addmissionYear = lineSplit[1].trim();
                String ID = lineSplit[2].trim();
                String check = lineSplit[3].trim();
                System.out.println(name+","+addmissionYear+","+ID+","+check);
              
            }
            reader.close();
        }
        catch(FileNotFoundException e) {
            System.out.println("File not found.");
            e.printStackTrace();
        }
    }//GEN-LAST:event_SaveActionPerformed

